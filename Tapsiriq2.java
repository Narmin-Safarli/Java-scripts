public class Tapsiriq2 {
    public static void main(String[] args) {
        int x = 36;
        int y = 6;

        System.out.println("Toplamanın nəticəsi:" + (x+y));              //42
        System.out.println("Çıxılmanın nəticəsi:" + (x-y));              //30
        System.out.println("Vurmanın nəticəsi:" + (x*y));                //216
        System.out.println("Bölmənin nəticəsi:" + (x/y));                //6
        System.out.println("Qalıq bölmənin nəticəsi:" + (x%y));          //0
    }
}
